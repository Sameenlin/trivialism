package com.demo.po;

public class Dept {
	private int dno;
	private String dname;
	private String loc;
	
	public int getDeptNo() {
		return dno;
	}
	public void setDeptNo(int dno) {
		this.dno = dno;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	
}
